#include "ircserv.hpp"

bool	is_special	(char c)
{
	return ((c >= '[' && c <= '`')
			|| (c >= '{' && c <= '}'));
}

bool	is_hexdigit	(char c)
{
	return ((c >= 'A' && c <= 'F')
			|| isdigit(c));
}
